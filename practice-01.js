// parseInt practice
var num1 = 10; // It si a number
var num2 = "20"; //  It is a string
var convertToNumberNum2 = parseInt(num2);
var num3 = num1 + convertToNumberNum2;
console.log(num3);

// parseFloat practice
var num4 = 10;
var num5 = "20.65845578";
var convertToNumberNum5 = parseFloat(num5);
var num6 = num4 + convertToNumberNum5;
console.log(num6);

